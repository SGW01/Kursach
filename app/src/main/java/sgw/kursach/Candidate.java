package sgw.kursach;

public class Candidate {
    String name, email;
    int age, range1, range2, rangeAll;

    public Candidate(String name, String email, int age, int range1, int range2, int rangeAll) {
        this.name = name;
        this.email = email;
        this.age = age;
        this.range1 = range1;
        this.range2 = range2;
        this.rangeAll = rangeAll;
    }
}
